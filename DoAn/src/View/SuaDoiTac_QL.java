    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        if (maDTSave == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tác để cập nhật!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String tenNCC = txtTenNCC.getText();
        String diaChi = txtDiachi.getText();
        String sdt = txtSDT.getText();
        if (tenNCC.equals("") || diaChi.equals("") || sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!isNumeric(txtSDT.getText())) {
            JOptionPane.showMessageDialog(this, "Hãy nhập vào 1 số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (isNumeric(tenNCC)) {
            JOptionPane.showMessageDialog(this, "Tên NCC không được là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn cập nhật?", "Xác nhận",JOptionPane.YES_NO_OPTION );
        if (reply == JOptionPane.NO_OPTION || reply == JOptionPane.CLOSED_OPTION) {
            return;
        }
        try {
            DoiTac dt = new DoiTac(maDTSave, tenNCC, diaChi, sdt);
            DoiTacBUS bus = new DoiTacBUS();

            if (bus.updateDT(dt)) {
                JOptionPane.showMessageDialog(this, "Đã cập nhật thành công!");
                clearDT();
                loadDoiTacAll();
            } else {
                JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi, vui lòng thử lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SaveActionPerformed

