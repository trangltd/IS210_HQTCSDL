    public void loadDoiTacAll() {
        String[] header = {"Mã NCC", "Tên nhà cung cấp", "Địa chỉ", "SĐT"};
        DefaultTableModel dtm = new DefaultTableModel(header, 0);
        ArrayList<DoiTac> arr = new ArrayList<DoiTac>();
        arr = DoiTacBUS.getDoiTacAll();
        DoiTac dt = new DoiTac();
        for (int i = 0; i < arr.size(); i++) {
            dt = arr.get(i);
            int maDT = dt.getMaDT();
            System.out.println(dt.getMaDT());
            String tenncc = dt.getTenDT();
            String diaChi = dt.getDiaChi();
            String sdt = dt.getSDT();
            Object[] row = {maDT, tenncc, diaChi, sdt};
            dtm.addRow(row);
        }
        jTable1.setModel(dtm);
    }
    
    public void FindDoiTac() {
        DefaultTableModel SearchTable = (DefaultTableModel) jTable1.getModel();
        String option = (String) cbb_search.getSelectedItem();
        String search = txtSearch.getText();
        SearchTable.setRowCount(0);

        if (option.equals("Mã NCC") || option.equals("SĐT")) {
            if (!isNumeric(txtSearch.getText())) {
                JOptionPane.showMessageDialog(this, "Hãy nhập vào 1 số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        ArrayList<DoiTac> arr = new ArrayList<DoiTac>();
        arr = DoiTacBUS.timDoiTac(option, search);
        DoiTac dt = new DoiTac();
        for (int i = 0; i < arr.size(); i++) {
            dt = arr.get(i);
            int maDT = dt.getMaDT();
            String tenncc = dt.getTenDT();
            String diaChi = dt.getDiaChi();
            String sdt = dt.getSDT();
            Object[] row = {maDT, tenncc, diaChi, sdt};
            SearchTable.addRow(row);
        }
        if (arr.size() <= 0) {
            JOptionPane.showMessageDialog(this, "Thông tin tìm kiếm không tồn tại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    }
