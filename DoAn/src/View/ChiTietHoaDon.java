    public int checkChucVu() {
        NhanVienBUS bus = new NhanVienBUS();
        String chucVu = bus.getChucVu(user);
        if (chucVu.equals("Quản lý")) {
            return 1;
        } else if (chucVu.equals("Thu ngân")) {
            return 2;
        } else if (chucVu.equals("Bán hàng")) {
            return 3;
        } else {
            return 4;
        }
    }
    
    public void loadKMMua() {
        String[] header = {"Mã KM", "Tên khuyến mãi", "Phần trăm", "Ngày bắt đầu", "Ngày kết thúc"};
        DefaultTableModel dtm = new DefaultTableModel(header, 0);
        ArrayList<KhuyenMai> arr = new ArrayList<KhuyenMai>();
        arr = KhuyenMaiBUS.getKhuyenMaiHoaDon(String.valueOf(soHD));
        KhuyenMai km = new KhuyenMai();
        for (int i = 0; i < arr.size(); i++) {
            km = arr.get(i);
            int maKM = km.getMaKM();
            String tenKM = km.getTenKM();
            float phanTram = km.getPhanTram();
            String ngbd = km.toString(km.getNgBD());
            String ngkt = km.toString(km.getNgKT());
            Object[] row = {maKM, tenKM, phanTram, ngbd, ngkt};
            dtm.addRow(row);
        }
        KhuyenMaiMua.setModel(dtm);
    }
    
    public void loadSPMua() {
        String[] header = {"Mã SP", "Tên sản phẩm", "Giá", "Tên loại SP", "Màu sắc", "SL mua", "Ghi chú"};
        DefaultTableModel dtm = new DefaultTableModel(header, 0);
        ArrayList<SanPham> arr = new ArrayList<SanPham>();
        arr = SanPhamBUS.getSanPhamHoaDon(String.valueOf(soHD));
        SanPham dt = new SanPham();
        for (int i = 0; i < arr.size(); i++) {
            dt = arr.get(i);
            int maSP = dt.getMaSP();
            String tenSP = dt.getTenSP();
            long gia = dt.getGiaSP();
            int maloaiSP = dt.getMaLoaiSP();
            String tenloaiSP = SanPhamBUS.getTenLSP(maloaiSP);
            String mausac = dt.getMauSac();
            int slsan = dt.getSlsan();
            String ghichu = dt.getGhiChu();
            Object[] row = {maSP, tenSP, gia, tenloaiSP, mausac, slsan, ghichu};
            dtm.addRow(row);
        }
        SanPhamMua.setModel(dtm);
    }
    
    public void loadChiTiet() {
        HoaDon hd = HoaDonBUS.getChiTietHoaDon(soHD);
        NhanVienBUS busNV = new NhanVienBUS();
        KhachHangBUS busKH = new KhachHangBUS();
        
        String tenNV = busNV.getTenNV(String.valueOf(hd.getMaNV()));
        String tenKH = "Ản danh";
        if (String.valueOf(hd.getMaKH()) != null) {
            tenKH = busKH.getTenKH(String.valueOf(hd.getMaKH()));
        } 
        long triGia = (long) hd.getTriGia();
        float chiKhau = hd.getChietKhau();
        LocalDate ngayhd = hd.getNgayHD();
        
        txtSoHD.setText(String.valueOf(soHD));
        txtTenNV.setText(tenNV);
        txtTenKH.setText(tenKH);
        txtTriGia.setText(String.valueOf(triGia));
        txtChietKhau.setText(String.valueOf(chiKhau));
        txtNgayHD.setText(ngayhd.toString());
        
        loadKMMua();
        loadSPMua();
   }
    
