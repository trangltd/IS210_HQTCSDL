    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        if (maNVSave == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn nhân viên để cập nhật!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String hoten = txtHoTen.getText();
        String diaChi = txtDiaChi.getText();
        String cmnd = txtCMND.getText();
        String sdt = txtSDT.getText();
        String ngsinh = String.format("%1$td/%1$tm/%1$tY", datePickerNgSinh.getDate());
        String gioitinh = (String) cbbGioiTinh.getSelectedItem();
        String chucvu = (String) cbbChucVu.getSelectedItem();
        String ngvl = String.format("%1$td/%1$tm/%1$tY", datePickerNgVL.getDate());
        String luongcb = txtLuongCB.getText();
        if (hoten.equals("") || diaChi.equals("") || cmnd.equals("") || sdt.equals("") || ngsinh.equals("")
                || gioitinh.equals("") || chucvu.equals("") || ngvl.equals("") || luongcb.equals("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String decimalPattern = "([0-9]*)\\.([0-9]*)"; 
        if (Pattern.matches(decimalPattern, luongcb)) {
            JOptionPane.showMessageDialog(this, "Số không được là kiểu thập phân", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        if (!isNumeric(txtCMND.getText()) || !isNumeric(txtSDT.getText()) || !isNumeric(txtLuongCB.getText())) {
            JOptionPane.showMessageDialog(this, "CMND/SĐT/Lương cơ bản phải là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (isNumeric(hoten)) {
            JOptionPane.showMessageDialog(this, "Họ tên không được là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (Integer.parseInt(luongcb) <= 0) {
            JOptionPane.showMessageDialog(this, "Lương không được nhỏ hơn 0!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn cập nhật?", "Xác nhận",JOptionPane.YES_NO_OPTION );
        if (reply == JOptionPane.NO_OPTION || reply == JOptionPane.CLOSED_OPTION) {
            return;
        }
        try {
            int luong = Integer.parseInt(luongcb);
            DateTimeFormatter fIn = DateTimeFormatter.ofPattern("dd/MM/yyyy");  // As a habit, specify the desired/expected locale, though in this case the locale is irrelevant.
            LocalDate ldNgS = LocalDate.parse( ngsinh , fIn );
            LocalDate ldNgVL = LocalDate.parse( ngvl , fIn );
            NhanVien nv = new NhanVien(maNVSave, hoten, gioitinh, diaChi, sdt, cmnd, chucvu, luong, ldNgS, ldNgVL);
            NhanVienBUS bus = new NhanVienBUS();

            if (bus.updateNV(nv)) {
                JOptionPane.showMessageDialog(this, "Đã lưu thành công!");
                clearNV();
                loadNhanVienAll();
            } else {
                JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi, vui lòng thử lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SaveActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int rowSelected = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        DateFormat sourceFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date ngsinh, ngvl;
        try {
            ngsinh = sourceFormat.parse((String) model.getValueAt(rowSelected, 5));
            ngvl = sourceFormat.parse((String) model.getValueAt(rowSelected, 8));
            ZoneId defaultZoneId = ZoneId.systemDefault();
            Instant instantNgSinh = ngsinh.toInstant();
            Instant instantNgVL = ngvl.toInstant();
            LocalDate localDateNgSinh = instantNgSinh.atZone(defaultZoneId).toLocalDate();
            LocalDate localDateNgVL = instantNgVL.atZone(defaultZoneId).toLocalDate();

            txtHoTen.setText((String) model.getValueAt(rowSelected, 1));
            txtDiaChi.setText((String) model.getValueAt(rowSelected, 2));
            txtCMND.setText((String) model.getValueAt(rowSelected, 3));
            txtSDT.setText((String) model.getValueAt(rowSelected, 4));
            datePickerNgSinh.setDate(localDateNgSinh);
            cbbGioiTinh.setSelectedItem((String) model.getValueAt(rowSelected, 6));
            cbbChucVu.setSelectedItem((String) model.getValueAt(rowSelected, 7));
            datePickerNgVL.setDate(localDateNgVL);
            txtLuongCB.setText(String.valueOf(model.getValueAt(rowSelected, 9)));
            String ma = model.getValueAt(rowSelected, 0).toString();
            int maNV = Integer.parseInt(ma);
            maNVSave = maNV;
        } catch (ParseException ex) {
            Logger.getLogger(SuaNhanVien_QL.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jTable1MouseClicked

