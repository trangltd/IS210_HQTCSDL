    public void LayReport(int a) throws SQLException, JRException {
        int maphieunhap = a;
        Hashtable map = new Hashtable();
        JasperReport report = JasperCompileManager.compileReport("src\\report\\Phieunhap.jrxml");
        
        map.put("Maphieunhap", maphieunhap);
        try {
            Connection con = ConnectionUtils.getMyConnection();
            JasperPrint p = JasperFillManager.fillReport(report, map, con);
            JasperViewer.viewReport(p, false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThemNhapHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void loadThangDoanhSo() {
        ArrayList<Integer> arr = new ArrayList<Integer>();
        arr = DoanhThuBUS.getThangDoanhSo();
        cbbThangDS.setModel(new DefaultComboBoxModel(arr.toArray()));
    }
    
    public void loadNamDoanhSo() {
        ArrayList<Integer> arr = new ArrayList<Integer>();
        arr = DoanhThuBUS.getNamDoanhSo();
        cbbNamDS.setModel(new DefaultComboBoxModel(arr.toArray()));
    }
    
    public void LayReportDoanhThuThang(int a, int b) throws SQLException, JRException {
        int thang = a;
        int nam = b;
        Hashtable map = new Hashtable();
        JasperReport report = JasperCompileManager.compileReport("src\\report\\report1.jrxml");
        
        map.put("thang", thang);
        map.put("nam", nam);
        try {
            Connection con = ConnectionUtils.getMyConnection();
            JasperPrint p = JasperFillManager.fillReport(report, map, con);
            JasperViewer.viewReport(p, false);
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }
